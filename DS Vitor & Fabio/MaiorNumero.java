public class MaiorNumero {
    public static void main(String[] args) {
        int num1 = 42;
        int num2 = 56;

        int maiorNumero;

        if (num1 > num2) {
            maiorNumero = num1;
        } else {
            maiorNumero = num2;
        }

        System.out.println("O maior número é: " + maiorNumero);
    }
}
