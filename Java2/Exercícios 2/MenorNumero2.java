public class MenorNumero2 {
    public static void main(String[] args) {
        int num1 = 42;
        int num2 = 56;

        int MenorNumero;

        if (num1 < num2) {
            MenorNumero = num1;
        } else {
            MenorNumero = num2;
        }

        System.out.println("O menor número é: " + MenorNumero);
    }
}

